package main

import "fmt"

func main() {
	// print stmt
	fmt.Println("Hello, world.")

	// variable declaration, initialization
	var a int32

	a = 15
	fmt.Println("value for a =", a)

	// dataType
	// boolean dataType
	var b bool

	b = true

	fmt.Println("value of b = ", b)

	// dataType
	// float
	var c float32

	c = 15.01

	fmt.Println("value of c = ", c)

}
